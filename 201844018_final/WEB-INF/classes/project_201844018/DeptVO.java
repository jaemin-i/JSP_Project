package project_201844018;

public class DeptVO {
	private String dept_no;
	private String dept_name;
	private int dept_proposer;
	private int dept_personnel;
	
	public String getDept_no() {
		return dept_no;
	}
	public void setDept_no(String dept_no) {
		this.dept_no = dept_no;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	public int getDept_proposer() {
		return dept_proposer;
	}
	public void setDept_proposer(int dept_proposer) {
		this.dept_proposer = dept_proposer;
	}
	public int getDept_personnel() {
		return dept_personnel;
	}
	public void setDept_personnel(int dept_personnel) {
		this.dept_personnel = dept_personnel;
	}

}
